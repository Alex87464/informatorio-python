"""
14-Crea una función llamada encontrar_maximo que tome una lista de números
como parámetro y devuelva el número máximo de la lista.
"""

def encontrar_maximo(lista_numeros):
    return print(max(lista_numeros)) # La funcion max retorna el valor máximo de un iterable    
    
encontrar_maximo([20, 14, 3, 9, 44, 32, 5]) # Debería retornar 44 dado que es el número mas grande de la lista

